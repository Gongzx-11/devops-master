package gzx.devops.model;

/**
 * 基础实体（带id）
 */
public abstract class BaseModel extends BaseJsonModel {
    private String id;
    private String name;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }
}
